import { storyblokInit, apiPlugin } from '@storyblok/react/rsc'
import { Manrope } from 'next/font/google'
import { getStoryblokApi, type StoryblokClient } from '@storyblok/react/rsc'
import StoryblokProvider from '../components/StoryblokProvider'
import { Header } from './components/Header/Header'
import { Footer } from './components/Footer/Footer'
import '../../scss/styles.scss'

storyblokInit({
  accessToken: 'HAhQiRUwaIYEaJfUHWncWgtt',
  use: [apiPlugin]
})

const manrope = Manrope({
  subsets: ['latin'],
  variable: '--ff-primary',
  display: 'swap'
})

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
  icons: {
    icon: '/favicon.png'
  }
}

// eslint-disable-next-line @typescript-eslint/explicit-function-return-type
export default async function RootLayout({
  children
}: {
  children: React.ReactNode
}) {
  const { data } = await fetchData()
  return (
    <StoryblokProvider>
      <html lang="en" className={`${manrope.variable}`}>
        <body>
          <Header navigation={data.story.content.navigation} />
          {children}
          <Footer
            text={data.story.content.footerText}
            facebook={data.story.content.facebook}
            twitter={data.story.content.twitter}
            instagram={data.story.content.instagram}
          />
        </body>
      </html>
    </StoryblokProvider>
  )
}

// eslint-disable-next-line @typescript-eslint/explicit-function-return-type
async function fetchData() {
  const storyblokApi: StoryblokClient = getStoryblokApi()

  const response = await storyblokApi.get('cdn/stories/global', {
    version: 'draft',
    resolve_relations: 'global.navigation'
  })

  return response
}
